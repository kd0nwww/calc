def multiply(num1: int, num2: int) -> str:
    return f"{num1} * {num2} = {num1 * num2}"

def add(num1: int, num2: int) -> str:
    return f"{num1} + {num2} = {num1 + num2}"

def divide(num1: int, num2: int) -> str:
    return f"{num1} / {num2} = {num1 / num2}"

def subtract(num1: int, num2: int) -> str:
    return f"{num1} - {num2} = {num1 - num2}"